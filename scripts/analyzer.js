// ═══════════════════════════════════════════════════════════════
//  كشف المواضيع المتقدم (مع N-Grams + Stemming + أوزان)
// ═══════════════════════════════════════════════════════════════
function detectThemes(text) {
    const scores = {};
    for (const [theme, config] of Object.entries(TOPICS)) {
        let score = 0;
        // كلمات مفردة مع جذع
        for (const kw of config.keywords) score += deepMatch(text, kw);
        // عبارات متعددة الكلمات
        for (const ph of config.phrases) score += matchPhrase(text, ph) * 3;
        scores[theme] = Math.round(score * config.weight);
    }

    const sorted = Object.entries(scores).sort((a, b) => b[1] - a[1]);
    const primary = sorted[0][1] > 0 ? sorted[0][0] : 'عام';
    const secondary = sorted[1] && sorted[1][1] > 0 ? sorted[1][0] : null;
    const tertiary = sorted[2] && sorted[2][1] > 0 ? sorted[2][0] : null;

    // حساب الثقة
    const total = Object.values(scores).reduce((a, b) => a + b, 0);
    const confidence = total > 0 ? Math.round((sorted[0][1] / total) * 100) : 0;

    return { all: scores, primary, secondary, tertiary, confidence };
}

