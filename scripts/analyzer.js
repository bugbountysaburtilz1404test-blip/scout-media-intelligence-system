//  المحرك الرئيسي
// ═══════════════════════════════════════════════════════════════
function run() {
    console.log('═══════════════════════════════════════════════');
    console.log('   🧠 محرك التحليل الذكي — الإصدار 4.0');
    console.log('   الأقوى مع جذع عربي + N-Grams + NER + سياق');
    console.log('═══════════════════════════════════════════════');

    if (!fs.existsSync(RAW_FILE)) { console.error('❌ لا توجد بيانات.'); return; }
    const raw = JSON.parse(fs.readFileSync(RAW_FILE, 'utf8'));

    let themeH = 0, cityH = 0, sentH = 0, nerH = 0;

    const processed = raw.map((art, idx) => {
        const norm = normalize(art.content);
        const titleNorm = normalize(art.title);
        const full = titleNorm + ' ' + norm;
        const words = tokenize(full);

        const wordCount = norm.split(' ').filter(w => w.length > 0).length;
        const readMin = Math.ceil(wordCount / 200) || 1;
        let readTier = 'سريع';
        if (readMin >= 5) readTier = 'طويل';
        else if (readMin >= 2) readTier = 'متوسط';

        // ترددات الكلمات (مع جذع)
        const freqs = {};
        for (const w of words) {
            const s = stemArabic(w);
            const key = s.length >= 3 ? s : w;
            freqs[key] = (freqs[key] || 0) + 1;
        }
        // نُعيد الكلمات الأصلية (الأكثر شيوعاً لكل جذع)
        const origMap = {};
        for (const w of words) {
            const s = stemArabic(w);
            const key = s.length >= 3 ? s : w;
            if (!origMap[key] || w.length > origMap[key].length) origMap[key] = w;
        }
        const sortedKW = Object.entries(freqs).sort((a, b) => b[1] - a[1]).slice(0, 25);
        const top25 = sortedKW.map(([k, c]) => ({ word: origMap[k] || k, stem: k, count: c }));

        const themes = detectThemes(full);
        const cities = detectCities(full);
        const sentiment = analyzeSentiment(full, art.content);
        const entities = detectEntities(art.content);
        const style = analyzeStyle(art.content, wordCount);
        const quality = analyzeQuality(art, wordCount, top25.length, style, sentiment, themes.confidence, entities.count);

        if (themes.primary !== 'عام') themeH++;
        if (cities.primary) cityH++;
        if (sentiment.label !== 'محايد') sentH++;
        if (entities.count > 0) nerH++;

        if (idx % 1000 === 0 && idx > 0) console.log(`   ⏳ ${idx.toLocaleString()}...`);

        return {
            id: art.url?.split('id=')[1] || String(Date.now()),
            title: art.title, url: art.url, date: art.date, views: art.views,
            wordCount, readingTimeMin: readMin, readingTier: readTier,
            top20Keywords: top25,
            primaryTheme: themes.primary,
            secondaryTheme: themes.secondary,
            tertiaryTheme: themes.tertiary,
            themeConfidence: themes.confidence,
            themeScores: themes.all,
            mostMentionedCity: cities.primary,
            secondCity: cities.secondary,
            cityMentions: cities.all,
            citiesDetected: cities.count,
            sentiment,
            entities,
            writingStyle: style,
            contentQuality: {
                hasNumbers: style.hasNumbers, hasQuotes: style.hasQuotes,
                titleLength: (art.title || '').length,
                richnessScore: quality.score, grade: quality.grade
            }
        };
    });

    fs.writeFileSync(PROCESSED_FILE, JSON.stringify(processed, null, 2), 'utf8');

    const n = processed.length;
    console.log('');
    console.log(`   ✅ تم معالجة ${n.toLocaleString()} مقال`);
    console.log(`   📊 المواضيع: ${themeH}/${n} (${Math.round(themeH / n * 100)}%)`);
    console.log(`   🏙️  المدن: ${cityH}/${n} (${Math.round(cityH / n * 100)}%)`);
    console.log(`   💬 المشاعر: ${sentH}/${n} (${Math.round(sentH / n * 100)}%)`);
    console.log(`   👤 الكيانات: ${nerH}/${n} (${Math.round(nerH / n * 100)}%)`);
    console.log('═══════════════════════════════════════════════');
}

run();
