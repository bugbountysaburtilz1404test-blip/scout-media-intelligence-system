// ─── جذع عربي مبسط (Arabic Light Stemmer) ─────────────────
function stemArabic(word) {
    if (!word || word.length < 3) return word;
    let w = word;
    // حذف الـ التعريف
    w = w.replace(/^(ال|وال|بال|كال|فال|لل)/, '');
    // حذف اللواحق
    w = w.replace(/(ات|ون|ين|ان|تين|ية|يه|ها|هم|هن|نا|كم|كن)$/, '');
    // حذف البادئات
    w = w.replace(/^(يت|تت|مت|مس|است)/, '');
    if (w.length < 2) return word;
    return w;
}

// ─── تطبيع النص العربي ──────────────────────────────────────
function normalize(text) {
    if (!text) return '';
    return text
        .replace(/[إأآا]/g, "ا")
        .replace(/ؤ/g, "و")
        .replace(/ئ/g, "ي")
        .replace(/ة/g, "ه")
        .replace(/ى/g, "ي")
        .replace(/[\u064B-\u065F\u0670]/g, "")
        .replace(/[^\u0621-\u064A0-9A-Za-z\s]/g, " ")
        .replace(/\s+/g, ' ')
        .trim();
}

function tokenize(text) {
    return text.split(' ').filter(w => w.length > 2 && !STOP.has(w) && isNaN(w));
}

function matchCount(text, keyword) {
    const k = normalize(keyword);
    if (!k || k.length < 2) return 0;
    const esc = k.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const m = text.match(new RegExp(`(?:^|\\s)${esc}(?:\\s|$)`, 'g'));
    return m ? m.length : 0;
}

// يتحقق من وجود عبارة متعددة الكلمات (بحث ضبابي)
function matchPhrase(text, phrase) {
    const words = normalize(phrase).split(' ').filter(w => w.length > 1);
    if (words.length <= 1) return matchCount(text, phrase);
    // نبحث عن كل الكلمات قريبة من بعض (ضمن 5 كلمات)
    const idx = [];
    for (const w of words) {
        const pos = text.indexOf(w);
        if (pos === -1) return 0;
        idx.push(pos);
    }
    const spread = Math.max(...idx) - Math.min(...idx);
    return spread < 80 ? 1 : 0;
}

// كشف متقدم يجمع بين المطابقة المباشرة والجذعية
function deepMatch(text, keyword) {
    let score = matchCount(text, keyword);
    // مطابقة الجذع
    const stem = stemArabic(normalize(keyword));
    if (stem.length >= 3 && stem !== normalize(keyword)) {
        const stemMatches = matchCount(text, stem);
        score += Math.floor(stemMatches * 0.5); // وزن أقل للجذعية
    }
    return score;
}

