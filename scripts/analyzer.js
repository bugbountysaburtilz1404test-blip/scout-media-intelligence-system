function detectCities(text) {
    const freqs = {};
    for (const city of CITIES) {
        const c = matchCount(text, city);
        if (c > 0) {
            const canon = CITY_NORM[city] || city;
            freqs[canon] = (freqs[canon] || 0) + c;
        }
    }
    const sorted = Object.entries(freqs).sort((a, b) => b[1] - a[1]);
    return {
        primary: sorted.length > 0 ? sorted[0][0] : null,
        secondary: sorted.length > 1 ? sorted[1][0] : null,
        all: freqs,
        count: sorted.length
    };
}

// ═══════════════════════════════════════════════════════════════
//  تحليل المشاعر المتقدم (300+ كلمة + مُعدّلات + سياق)
// ═══════════════════════════════════════════════════════════════
function analyzeSentiment(text, originalContent) {
    let posScore = 0, negScore = 0;
    const posWords = [], negWords = [];

    // كلمات إيجابية
    for (const w of POSITIVE.strong) { const c = deepMatch(text, w); posScore += c * 3; if (c) posWords.push(w); }
    for (const w of POSITIVE.medium) { const c = deepMatch(text, w); posScore += c * 2; if (c) posWords.push(w); }
    for (const w of POSITIVE.mild) { const c = deepMatch(text, w); posScore += c * 1; if (c) posWords.push(w); }

    // كلمات سلبية
    for (const w of NEGATIVE.strong) { const c = deepMatch(text, w); negScore += c * 3; if (c) negWords.push(w); }
    for (const w of NEGATIVE.medium) { const c = deepMatch(text, w); negScore += c * 2; if (c) negWords.push(w); }
    for (const w of NEGATIVE.mild) { const c = deepMatch(text, w); negScore += c * 1; if (c) negWords.push(w); }

    // مُعدّلات التكثيف
    let intensifierCount = 0;
    for (const int of INTENSIFIERS) intensifierCount += matchCount(text, int);
    if (intensifierCount > 0) {
        if (posScore > negScore) posScore = Math.round(posScore * (1 + intensifierCount * 0.15));
        else negScore = Math.round(negScore * (1 + intensifierCount * 0.15));
    }

    // كشف النفي (يعكس المعنى)
    for (const neg of NEGATORS) {
        const normNeg = normalize(neg);
        for (const pw of POSITIVE.strong.concat(POSITIVE.medium).slice(0, 20)) {
            if (matchPhrase(text, neg + ' ' + pw) > 0) {
                posScore = Math.max(0, posScore - 2);
                negScore += 1;
            }
        }
    }

    const total = posScore + negScore;
    let label = 'محايد', score = 0, intensity = 'منخفض';

    if (total > 0) {
        score = Math.round(((posScore - negScore) / total) * 100);
        if (score > 15) { label = 'إيجابي'; intensity = score > 60 ? 'عالي' : score > 35 ? 'متوسط' : 'منخفض'; }
        else if (score < -15) { label = 'سلبي'; intensity = score < -60 ? 'عالي' : score < -35 ? 'متوسط' : 'منخفض'; }
    }

    return {
        label, score, intensity,
        positive: posScore, negative: negScore,
        topPositive: [...new Set(posWords)].slice(0, 5),
        topNegative: [...new Set(negWords)].slice(0, 5)
    };
}

// ═══════════════════════════════════════════════════════════════
//  كشف الكيانات المتقدم (NER)
// ═══════════════════════════════════════════════════════════════
function detectEntities(content) {
    if (!content) return { people: [], organizations: [], count: 0 };
    const people = new Set(), orgs = new Set();

    for (const prefix of TITLE_PREFIXES) {
        const regex = new RegExp(`${prefix}\\s+([\\u0621-\\u064A]+(?:\\s+[\\u0621-\\u064A]+){0,4})`, 'g');
        let m;
        while ((m = regex.exec(content)) !== null) {
            const name = (prefix + ' ' + m[1]).trim();
            if (name.length > 6 && name.length < 70 && name.split(' ').length >= 2) people.add(name);
        }
    }

    for (const kw of ORG_KEYWORDS) {
        const regex = new RegExp(`${kw}\\s+([\\u0621-\\u064A]+(?:\\s+[\\u0621-\\u064A]+){0,5})`, 'g');
        let m;
        while ((m = regex.exec(content)) !== null) {
            const org = (kw + ' ' + m[1]).trim();
            if (org.length > 6 && org.length < 90 && org.split(' ').length >= 2) orgs.add(org);
        }
    }

    return {
        people: [...people].slice(0, 8),
        organizations: [...orgs].slice(0, 8),
        count: people.size + orgs.size
    };
}

